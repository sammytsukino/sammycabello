export function paletteToRgbCss(pal) {
  return pal.map(([r, g, b]) => {
    const R = Math.round(r * 255)
    const G = Math.round(g * 255)
    const B = Math.round(b * 255)
    return `rgb(${R} ${G} ${B})`
  })
}
